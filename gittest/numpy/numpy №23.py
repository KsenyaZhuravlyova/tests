# Создайте настраиваемый dtype, который описывает цвет как четыре байта без знака (RGBA)

import numpy as np

# Определение настраиваемого dtype для RGBA цвета
rgba_dtype = np.dtype([
    ('R', np.uint8),  # Красный канал (8 бит)
    ('G', np.uint8),  # Зеленый канал (8 бит)
    ('B', np.uint8),  # Синий канал (8 бит)
    ('A', np.uint8)   # Альфа-канал (прозрачность) (8 бит)
])

# Использование настраиваемого dtype для создания массива цветов
colors = np.array([(255, 0, 0, 255),  # Красный
                   (0, 255, 0, 255),   # Зеленый
                   (0, 0, 255, 255),   # Синий
                   (0, 0, 0, 0)],      # Прозрачный (черный)
                  dtype=rgba_dtype)

print(colors)

